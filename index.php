<?php
// Функция для сложения двух чисел
function add($a, $b) {
    return $a + $b;
}

// Функция для умножения двух чисел
function multiply($a, $b) {
    return $a * $b;
}

// Выводим список функций
echo "Список доступных функций:\n";
echo "add(\$a, \$b) - Сложение двух чисел\n";
echo "multiply(\$a, \$b) - Умножение двух чисел\n";

// Примеры использования функций
echo "\nПримеры использования:\n";
echo "add(2, 3) = " . add(2, 3) . "\n";
echo "multiply(2, 3) = " . multiply(2, 3) . "\n";
?>
